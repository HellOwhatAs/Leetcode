from typing import List,Tuple,Callable
class Solution:
    def neibours(self,point:Tuple[int,int],visitable:int)->List[Tuple[int,int]]:
        x,y=point
        ret=[]
        if 0<=x-1<self.n and 0<=y<self.n and self.grid[x-1][y]==visitable:ret.append((x-1,y))
        if 0<=x+1<self.n and 0<=y<self.n and self.grid[x+1][y]==visitable:ret.append((x+1,y))
        if 0<=x<self.n and 0<=y-1<self.n and self.grid[x][y-1]==visitable:ret.append((x,y-1))
        if 0<=x<self.n and 0<=y+1<self.n and self.grid[x][y+1]==visitable:ret.append((x,y+1))
        return ret
    def bfs(self,points:List[Tuple[int,int]],visitable:int):
        ret=set()
        for i in points:
            for j in self.neibours(i,visitable):
                ret.add(j)
        return list(ret)
    def shortestBridge(self, grid: List[List[int]]) -> int:
        self.n,self.grid=len(grid),grid
        for i in range(self.n):
            for j in range(self.n):
                if grid[i][j]:break
            if grid[i][j]:break
        a=[(i,j)]
        while a:
            for i in a:self.grid[i[0]][i[1]]=2
            a=self.bfs(a,1)
        a.clear()
        for i in range(self.n):
            for j in range(self.n):
                if grid[i][j]==2:
                    a.append((i,j))
        ret=0
        while a:
            for i in a:
                for j in self.neibours(i,1):
                    return ret
                self.grid[i[0]][i[1]]=2
            ret+=1
            a=self.bfs(a,0)
