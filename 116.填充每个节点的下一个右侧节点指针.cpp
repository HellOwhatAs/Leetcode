#include<iostream>
using namespace std;
class Solution {
public:
    Node* connect(Node* root) {
        if(root==nullptr)return root;
        connect(root->left);
        connect(root->right);
        for(auto a=root->left,b=root->right;a!=nullptr&&b!=nullptr;a=a->right,b=b->left){
            a->next=b;
        }
        return root;
    }
};
